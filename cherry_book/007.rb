a = [1]
b = [2, 3]
a.concat(b) #=> [1, 2, 3]

a = [1]
b = [2, 3]
a + b #=> [1, 2, 3]

a = [1, 2, 3]
b = [3, 4, 5]
a | b #=> [1, 2, 3, 4, 5]

a = [1, 2, 3]
b = [3, 4, 5]
a - b #=> [1, 2]

a = [1, 2, 3]
b = [3, 4, 5]
a & b #=>[3]

require 'set'
a = Set.new([1, 2, 3])
b = Set.new([3, 4, 5])
a | b #=> #<Set: {1, 2, 3, 4, 5}>
a - b #=> #<Set: {1, 2}>
a & b #=> #<Set: {3}>
