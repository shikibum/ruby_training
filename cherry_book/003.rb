[].class #=> Array
a = [1 ,2 ,3]
b = [1, 'haku', 2, 'yamato']
b[0] #=> 1
b[3] #=> 'yamato'
b.size #=> 4
b.length #=> 4

a = [1 ,2 ,3]
a[1] = 20
a #=> [1, 20, 3]
a[4] = 50
a #=> [1, 20, 3, nil, 50]

a = []
a << 1
a << 2
a << 3
a #=> [1, 2, 3]

a = [1, 2, 3]
a.delete_at(1) #=> 2
a #=> [1, 3]

# 多重代入
a,b = 1, 2
#=> [1, 2]
# 右辺の数が少ない場合はnilが入る
c, d = [10]
c #=> [10]
d #=> nil
# 右辺の数が多い場合ははみ出した値が切り捨てられる
e, f = [100, 200, 300]
e #=> 100
f #=> 200

# 商と余りを配列で返す
14.divmod(3)
#=> [4, 2]
# 戻り値を配列のまま受け取る
quo_rem = 14.divmod(3)
#=> [4, 2]
"商=#{quo_rem[0]}, 余り=#{quo_rem[1]}"
#=> "商=4, 余り=2"
# 多重代入で別々の変数として受け取る
quotient, remainder = 14.divmod(3)
"商=#{quotient}, 余り=#{remainder}"

